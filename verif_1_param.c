#include "ft_ls.h"

int		verif_1_param(char *sr)
{
	int		i;

	i = 0;
	if (sr[i] != '-')
		return (0);
	while (sr[i])
	{
		i++;
		if (sr[i] == '-')
			return (0);
	}
	return (1);
}
